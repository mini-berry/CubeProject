uint16_t LCD_ReadPoint(uint16_t x, uint16_t y)
{
	uint16_t r = 0, g = 0, b = 0;
	if (x >= lcddev.width || y >= lcddev.height)
		return 0; // �����˷�Χ,ֱ�ӷ���
	LCD_SetCursor(x, y);

	LCD_WR_REG(0X2E); // 9341/6804/3510 ���Ͷ�GRAMָ��

	if (LCD_DATA_ADDRESS)
		r = 0;			  // dummy Read
	r = LCD_DATA_ADDRESS; // ʵ��������ɫ

	b = LCD_DATA_ADDRESS;
	g = r & 0XFF; // ����9341/5310,��һ�ζ�ȡ����RG��ֵ,R��ǰ,G�ں�,��ռ8λ
	g <<= 8;

	return (((r >> 11) << 11) | ((g >> 10) << 5) | (b >> 11)); // ILI9341/NT35310��Ҫ��ʽת��һ��
}

void LCD_DrawLine(uint16_t x1, uint16_t y1, uint16_t x2, uint16_t y2, uint16_t color)
{
	uint16_t t;
	int xerr = 0, yerr = 0, delta_x, delta_y, distance;
	int incx, incy, uRow, uCol;
	delta_x = x2 - x1; // ������������
	delta_y = y2 - y1;
	uRow = x1;
	uCol = y1;
	if (delta_x > 0)
		incx = 1; // ���õ�������
	else if (delta_x == 0)
		incx = 0; // ��ֱ��
	else
	{
		incx = -1;
		delta_x = -delta_x;
	}
	if (delta_y > 0)
		incy = 1;
	else if (delta_y == 0)
		incy = 0; // ˮƽ��
	else
	{
		incy = -1;
		delta_y = -delta_y;
	}
	if (delta_x > delta_y)
		distance = delta_x; // ѡȡ��������������
	else
		distance = delta_y;
	for (t = 0; t <= distance + 1; t++) // �������
	{
		LCD_set_dot(uRow, uCol, color); // ����
		xerr += delta_x;
		yerr += delta_y;
		if (xerr > distance)
		{
			xerr -= distance;
			uRow += incx;
		}
		if (yerr > distance)
		{
			yerr -= distance;
			uCol += incy;
		}
	}
}

void Draw_Circle(uint16_t x0, uint16_t y0, uint16_t r, uint16_t color)
{
	int a, b;
	int di;
	a = 0;
	b = r;
	di = 3 - (r << 1); // �ж��¸���λ�õı�־
	while (a <= b)
	{
		LCD_set_dot(x0 + a, y0 - b, color); // 5
		LCD_set_dot(x0 + b, y0 - a, color); // 0
		LCD_set_dot(x0 + b, y0 + a, color); // 4
		LCD_set_dot(x0 + a, y0 + b, color); // 6
		LCD_set_dot(x0 - a, y0 + b, color); // 1
		LCD_set_dot(x0 - b, y0 + a, color);
		LCD_set_dot(x0 - a, y0 - b, color); // 2
		LCD_set_dot(x0 - b, y0 - a, color); // 7
		a++;
		// ʹ��Bresenham�㷨��Բ
		if (di < 0)
			di += 4 * a + 6;
		else
		{
			di += 10 + 4 * (a - b);
			b--;
		}
	}
}

void LCD_ShowChar(uint16_t x, uint16_t y, uint8_t num, uint8_t size, uint16_t bg, uint16_t color)
{
	uint8_t temp, t1, t;
	uint16_t y0 = y;

	// ���ô���
	num = num - ' '; // �õ�ƫ�ƺ��ֵ

	for (t = 0; t < size; t++)
	{
		if (size == 12)
		{
			temp = asc2_1206[num][t];
		} // ����1206����
		else
		{
			temp = asc2_1608[num][t];
		} // ����1608����
		for (t1 = 0; t1 < 8; t1++)
		{
			if (temp & 0x80)
			{
				LCD_set_dot(x, y, color);
			}
			else
			{
				if (bg != color)
				{
					LCD_set_dot(x, y, bg);
				}
			}
			temp <<= 1;
			y++;
			if (x >= lcddev.width)
			{
				return;
			} // ��������
			if ((y - y0) == size)
			{
				y = y0;
				x++;
				if (x >= lcddev.width)
				{
					return;
				} // ��������
				break;
			}
		}
	}
}

void LCD_ShowString(uint16_t x, uint16_t y, uint8_t *p, uint8_t size, uint16_t bg, uint16_t color)
{
	while (*p != '\0')
	{

		if (x >= lcddev.width || *p == '\n')
		{
			x = 0;
			y += size;
		}
		else
		{
			LCD_ShowChar(x, y, *p, size, bg, color);
			x += (size / 2);
		}
		p++;
	}
}
