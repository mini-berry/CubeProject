void LCD_Scan_Dir(uint8_t dir)
{
	uint16_t regval = 0;
	uint8_t dirreg = 0;
	uint16_t temp;
	if (lcddev.dir == 1)
	{
		switch (dir)
		{
		case 0:
			dir = 6;
			break;
		case 1:
			dir = 7;
			break;
		case 2:
			dir = 4;
			break;
		case 3:
			dir = 5;
			break;
		case 4:
			dir = 1;
			break;
		case 5:
			dir = 0;
			break;
		case 6:
			dir = 3;
			break;
		case 7:
			dir = 2;
			break;
		}
	}

	switch (dir)
	{
	case L2R_U2D: // ������,���ϵ���
		regval |= (0 << 7) | (0 << 6) | (0 << 5);
		break;
	case L2R_D2U: // ������,���µ���
		regval |= (1 << 7) | (0 << 6) | (0 << 5);
		break;
	case R2L_U2D: // ���ҵ���,���ϵ���
		regval |= (0 << 7) | (1 << 6) | (0 << 5);
		break;
	case R2L_D2U: // ���ҵ���,���µ���
		regval |= (1 << 7) | (1 << 6) | (0 << 5);
		break;
	case U2D_L2R: // ���ϵ���,������
		regval |= (0 << 7) | (0 << 6) | (1 << 5);
		break;
	case U2D_R2L: // ���ϵ���,���ҵ���
		regval |= (0 << 7) | (1 << 6) | (1 << 5);
		break;
	case D2U_L2R: // ���µ���,������
		regval |= (1 << 7) | (0 << 6) | (1 << 5);
		break;
	case D2U_R2L: // ���µ���,���ҵ���
		regval |= (1 << 7) | (1 << 6) | (1 << 5);
		break;
	}
	dirreg = 0X36;

	regval |= 0X08;
	LCD_WriteReg(dirreg, regval);
	if ((regval & 0X20) || lcddev.dir == 1)
	{
		if (lcddev.width < lcddev.height) // ����X,Y
		{
			temp = lcddev.width;
			lcddev.width = lcddev.height;
			lcddev.height = temp;
		}
	}
	else
	{
		if (lcddev.width > lcddev.height) // ����X,Y
		{
			temp = lcddev.width;
			lcddev.width = lcddev.height;
			lcddev.height = temp;
		}
	}
	LCD_WR_REG(lcddev.setxcmd);
	LCD_WR_DATA(0);
	LCD_WR_DATA(0);
	LCD_WR_DATA((lcddev.width - 1) >> 8);
	LCD_WR_DATA((lcddev.width - 1) & 0XFF);
	LCD_WR_REG(lcddev.setycmd);
	LCD_WR_DATA(0);
	LCD_WR_DATA(0);
	LCD_WR_DATA((lcddev.height - 1) >> 8);
	LCD_WR_DATA((lcddev.height - 1) & 0XFF);
}

